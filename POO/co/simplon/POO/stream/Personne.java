package co.simplon.POO.stream;

public class Personne {
	
	String nom;
	int taille;
	int poid;
	int longueurCheveux;
	String couleurCheveux;
	String couleurYeux;
	String couleurPeau;
	boolean barbe;
	boolean moustache;
	boolean bouc; 

	
	public Personne( String nom, int taille, int poid, int longueurCheveux, String couleurCheveux, String couleurYeux, String couleurPeau,
			boolean barbe, boolean moustache, boolean bouc ) {
		this.nom = nom;
		this.taille = taille;
		this.poid = poid;
		this.longueurCheveux = longueurCheveux;
		this.couleurCheveux = couleurCheveux;
		this.couleurYeux = couleurYeux;
		this.couleurPeau = couleurPeau;
		this.barbe = barbe;
		this.moustache = moustache;
		this.bouc = bouc;

	} 

}
